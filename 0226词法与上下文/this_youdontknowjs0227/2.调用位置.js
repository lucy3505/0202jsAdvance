// 关于调用位置
//在理解this的绑定过程之前，我们了解一下调用位置，//^调用位置表示的是函数所被调用的位置，而不是其声明的位置。

// 如何知道函数的调用位置，最重要的是分析函数的调用栈（即为了到达当前执行位置所调用的所有函数）。//^那么调用位置就是当前正在执行函数的前一个调用中。

function baz() {
  //当前的调用栈是baz
  //当前的调用位置是全局作用域，即当前调用栈的前一个调用
  console.log('baz');
  bar();
}

function bar() {
  //当前调用栈是 baz-->bar
  //当前的调用位置是：baz
  console.log('bar');
  foo();
}

function foo() {
  //当前的调用栈是baz --> bar --> foo
  //当前调用位置是bar
}

baz(); //<-- baz的调用位置就是全局作用域

