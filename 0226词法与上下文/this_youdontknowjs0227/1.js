
函数可以自动引用合适的上下文对象。
this在任何情况下都不指向函数的词法作用域（词法作用域：声明时就确定好的作用域）。
在JS内部，作用域确实和对象类似，可见的标识符都是它的属性。但是作用域“对象”无法通过Js代码访问，他存在于JS引擎内部。

//*this到底是什么
this是在运行时进行绑定的，并不是在编写时绑定的。他的上下文取决于函数调用时的各种条件。this的绑定和函数声明的位置没有任何关系，只取决于函数的调用方法。


当函数被调用时，会创建上下文对象，里面有变量对象（AO），作用域链还有this
